# Генератор списка для первых 10 натуральных чисел, умноженных на 3
# range(1, 11) создает последовательность чисел от 1 до 10 (включительно).
# x * 3 умножает каждое число из последовательности на 3.
multiples_of_three = [x * 3 for x in range(1, 11)]

# Выводим результат
# Печатаем полученный список, содержащий числа, умноженные на 3.
print(multiples_of_three)
 